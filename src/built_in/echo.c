#include "twenty_one.h"

void	echo(char **args)
{
	size_t	i;

	i = 0;
	while (args[i])
	{
		ft_printf(1, args[i]);
		if (args[i + 1])
			ft_printf(1, " ");
		i++;
	}
	ft_printf(1, "\n");
}
